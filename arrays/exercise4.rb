# Arrays: Exercise 4
# What does each method return in the following example?

=begin
arr = [15, 7, 18, 5, 12, 8, 5, 1]

1. arr.index(5)

2. arr.index[5]

3. arr[5]
=end

# 1. returns 3 as that is the index of the first value of 5
# 2. gives an error (undefined method '[]' for the enumerator)
# 3. returns 8 as that is the value of index 5